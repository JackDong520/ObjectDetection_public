package graphjavabean.homepage;

public class GraphNumOfBug {

    private int numOfBugOne;
    private int numOfBugTwo;
    private int numOfBugThree;

    public GraphNumOfBug(int numOfBugOne, int numOfBugTwo, int numOfBugThree) {
        this.numOfBugOne = numOfBugOne;
        this.numOfBugTwo = numOfBugTwo;
        this.numOfBugThree = numOfBugThree;
    }

    public int getNumOfBugOne() {
        return numOfBugOne;
    }

    public void setNumOfBugOne(int numOfBugOne) {
        this.numOfBugOne = numOfBugOne;
    }

    public int getNumOfBugTwo() {
        return numOfBugTwo;
    }

    public void setNumOfBugTwo(int numOfBugTwo) {
        this.numOfBugTwo = numOfBugTwo;
    }

    public int getNumOfBugThree() {
        return numOfBugThree;
    }

    public void setNumOfBugThree(int numOfBugThree) {
        this.numOfBugThree = numOfBugThree;
    }
}
